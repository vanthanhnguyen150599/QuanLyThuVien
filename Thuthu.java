    public boolean checkEmail(String email)
    {
        String check = "^[a-z][a-z0-9]{0,}[@]{1}[a-z]{1,}\\.{1}[a-z]{1,}";
        return email.matches(check);
    }
    
    public boolean checkSdt(String sdt)
    {
        String check = "^[0][0-9]{9}";
        return sdt.matches(check);
    }
    
