    private void jButton_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_XoaActionPerformed
         // TODO add your handling code here:
        String MTT = jTextField_MTT.getText();
        if(MTT.length() == 0)
        {
            JOptionPane.showMessageDialog(this, "Nhập Mã thủ thư bạn muốn xóa");
        }
        else
        {
            if(kiemtraMATT(MTT)==true)
            {
                xoatheoMATT(MTT);
                
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Không tồn tại mã cần xóa");
            }
        }
        
    }//GEN-LAST:event_jButton_XoaActionPerformed

    private void jTable_ThuThuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_ThuThuMouseClicked
        int i = jTable_ThuThu.getSelectedRow();
        DefaultTableModel dtm = (DefaultTableModel) jTable_ThuThu.getModel();
        
        String ns[]= dtm.getValueAt(i, 4).toString().split("-");
        
        jTextField_MTT.setText(dtm.getValueAt(i, 0).toString());
        jTextField_Ho.setText(dtm.getValueAt(i, 1).toString());
        jTextField_Ten.setText(dtm.getValueAt(i, 2).toString());
        if(dtm.getValueAt(i, 3).toString().equals("NAM"))
        {
            jRadioButton_Nam.setSelected(true);
        }
        else
        {
            jRadioButton_Nu.setSelected(true);
        }
        jTextField_DiaChi.setText(dtm.getValueAt(i, 4).toString());
        jTextField_Email.setText(dtm.getValueAt(i, 5).toString());
        jTextField_Sdt.setText("0" + dtm.getValueAt(i, 6).toString());
    }//GEN-LAST:event_jTable_ThuThuMouseClicked

