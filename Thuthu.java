    public void napThuThu()
    {
        DefaultTableModel ntt = (DefaultTableModel) jTable_ThuThu.getModel();
        ntt.setNumRows(0);

        Connection connect = ketnoicsdl.layKetNoi();
        try
        {
            Statement st = connect.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM THUTHU");
            while(rs.next())
            {
             Vector<Object> vt = new Vector();
             String MATT = rs.getString("MATT");
             String HO = rs.getString("HO");
             String TEN = rs.getString("TEN");
             String PHAI = rs.getString("PHAI");
             String DIACHI = rs.getString("DIACHI");
             String EMAIL = rs.getString("EMAIL");
             int SDT = Integer.parseInt(rs.getString("SDT"));
             
             vt.add(MATT);
             vt.add(HO);
             vt.add(TEN);
             vt.add(PHAI);
             vt.add(DIACHI);
             vt.add(EMAIL);
             vt.add(SDT);
             //   System.out.println(vt);
             ntt.addRow(vt);
            }
        }
        catch (SQLException e)
        {
            System.out.println("Khong the nap thu thu");
        }
    }
    
    private void themThuThu(String MTT, String Ho, String Ten, String Phai, String DiaChi, String Email, String Sdt)
    {
        Connection ketNoi = ketnoicsdl.layKetNoi();
        String sql;
        sql = "insert into THUTHU values (?,?,?,?,?,?,?)";
        try
        {
            int SDT = Integer.valueOf(Sdt);
            PreparedStatement ps = ketNoi.prepareStatement(sql);
            ps.setString(1, MTT);
            ps.setString(2, Ho);
            ps.setString(3, Ten);
            ps.setString(4, Phai);
            ps.setString(5, DiaChi);
            ps.setString(6, Email);
            ps.setInt(7, SDT);
            ps.executeUpdate();
            
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
        }
    }
    
